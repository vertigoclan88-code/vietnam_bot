# Для отслеживания неповторяющихся советов
used_indices = []

def get_unique_tip():
    """Возвращает не повторявшийся ранее совет"""
    global used_indices
    available = [i for i in range(len(tips)) if i not in used_indices]
    
    if not available:  # Если всё использовано — сбрасываем историю
        used_indices = []
        available = list(range(len(tips)))
    
    chosen = random.choice(available)
    used_indices.append(chosen)
    return tips[chosen]

def send_weekly_tip():
    """Отправляет полезный факт в группу"""
    try:
        tip = get_unique_tip()
        message = (
            f"🌟 *Еженедельный гид по Нячангу и Фукуоку 2026* 🌟\n\n"
            f"{tip}\n\n"
            f"━━━━━━━━━━━━━━━━━━━━\n"
            f"💰 *Обмен валюты:* @obmenka24xmax\n"
            f"🚗 *Водительские права:* @kaliroling\n"
            f"🛂 *Визаран в Лаос:* @rootsudokali\n\n"
            f"🤖 *Бот обновлён по данным 2026 года*\n"
            f"📌 *Группа:* @phuquoclive"
        )
        bot.send_message(CHAT_ID, message, parse_mode='Markdown')
        print(f"✅ Совет отправлен! Осталось уникальных: {len(tips) - len(used_indices)}")
        return True
    except Exception as e:
        print(f"❌ Ошибка: {e}")
        return False

